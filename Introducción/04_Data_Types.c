#include <stdio.h>

int main() {
    // Data Types

    int myNum = 5;
    float myFloatNum = 5.99;
    char myLetter = 'D';

    printf("%d\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);

    // Basic Data Types

    // Data Type            Size            Example
    // int                  2 or 4 bytes    1
    // float                4 bytes         1.99
    // double               8 bytes         1.99999999
    // char                 1 byte          'A'

    // Basic Format Specifiers

    // Format Specifier         Data Type
    // %d or %i                 int
    // %f or %F                 float
    // %lf                      double
    // %c                       char
    // %s                       string

    return 0;
}